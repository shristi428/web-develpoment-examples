 <h2><b><u> Newfoundland </u></b><h2>
 <p>  The Newfoundland is a working dog from, you guessed it, Newfoundland. Unlike many larger breeds, the Newfie wasn't bred to be a guard dog.</br> Instead, its purpose was originally to help fishermen. The big, muscular dogs are able to haul nets and lines from boats, pull carts and, most importantly, fetch anything that falls overboard, including people.</br> The breed is an exceptional water dog and strong swimmer, and there have been many rescues of people out at sea credited to these big, gentle-natured dogs.</br>
Newfoundland dogs stand between 27-30 inches tall and weigh as much as 150 pounds. </br>They look even bigger because of their thick double coat, which keeps them warm even in icy water.</p></div>


