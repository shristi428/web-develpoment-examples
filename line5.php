 <h2><b><u> Great Dane</u></b><h2>
 <p> The Great Dane is a breed of German origin and its German name of Deutsche Dogge, means German mastiff. </br>However, before setting down official roots in Germany, the dogs that eventually became the Great Dane breed came from a crossbreed between English mastiffs and Irish wolfhounds.</br>
Though they aren't the heaviest dogs, reaching around 100-120 pounds, they are among the tallest. The average Great Dane stands around 28-30 inches tall but often they can be taller.</br> The world record holder for tallest dog was a Great Dane named Zeus who stood an astounding 44 inches tall.</br> However, these big dogs trade longevity for their size, and live only to be between 6 to 8 years old. Zeus died of old age at just 5 years old.</p></div>

