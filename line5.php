 <h2><b><u> Neapolitan mastiff</u></b><h2>
 <p> Mastiff breeds are certainly among the largest dogs in terms of sheer mass. The Neapolitan mastiff originated in southern Italy.</br> Used as a guard dog, the average male mastiff stands between 26-31 inches tall and weighs a hefty 130-155 pounds. Females are usually a little smaller, standing a few inches shorter and weighing 110-130 pounds.</br>
This breed is known for being fearless and protective of home and family, making it an ideal guard dog — but not an ideal warning system.</br> Mastiffs tend to be quiet, and are known for sneaking up on intruders rather than barking to warn them off.</br> Because of the breed's protective nature, you certainly don't want to stand between these dogs and their family, which makes this dog a breed only for owners well versed in dog training and able to put in the extensive time needed for socialization.</p></div>

