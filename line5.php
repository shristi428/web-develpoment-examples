 <h2><b><u>  Scottish Deerhound</u></b><h2>
 <p> Going back to the leggy breeds, the Scottish deerhound gives away its purpose and origin in its name.</br> Originating in Scotland well before recorded history, the breed is a courser, once used to hunt red deer and easily chasing down its prey. They are larger and heavier than greyhounds but are built similarly, with a lanky body meant for speed.</br>
Deerhounds can stand as tall as 32 inches and weigh as much as 110 pounds.</br> Though they aren't used for deer hunting today, the breed is kept alive by enthusiasts who use them for show and in some places, lure coursing.</p></div>

