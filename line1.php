          <h2><b><u>Origin</u></b></h2>
           <p>The origin of the domestic dog is not clear. It is known that the dog was the first domesticated species. The domestic dog is a member of the genus Canis (canines), which forms part of the wolf-like canids, and is the most widely abundant terrestrial carnivore.</br> The closest living relative of the dog is the gray wolf and there is no evidence of any other canine contributing to its genetic lineage.</br> The dog and the extant gray wolf form two sister clades, with modern wolves not closely related to the wolves that were first domesticated.</br>The archaeological record shows the first undisputed dog remains buried beside humans 14,700 years ago, with disputed remains occurring 36,000 years ago.These dates imply that the earliest dogs arose in the time of human hunter-gatherers and not at the dawn of agriculture.</br>
Where the genetic divergence of dog and wolf took place remains controversial, with the most plausible proposals spanning Western Europe, Central Asia, and East Asia.</br> This has been made more complicated by the most recent proposal that fits the available evidence, which is that an initial wolf population split into East and West Eurasian wolves, these were then domesticated independently before going extinct into two distinct dog populations between 14,000-6,400 years ago, and then the Western Eurasian dog population was partially and gradually replaced by East Asian dogs that were brought by humans at least 6,400 years ago.</hr></p>
